public class Users {
public int id;
public String name;
public String address;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
   

    public String getAddress() {
        return address;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    
}
